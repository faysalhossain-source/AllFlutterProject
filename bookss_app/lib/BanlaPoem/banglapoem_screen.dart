ব্যোম গুড়গুড়ির নল মুখ হইতে নামাইয়া নিমীলিতনেত্রে কহিলেন– আমি ঐক্যবাদী। একা গদ্যের দ্বারাই আমাদের সকল আবশ্যক সুসম্পন্ন হইতে পারিত, মাঝে হইতে পদ্য আসিয়া মানুষের মনোরাজ্যে একটা অনাবশ্যক বিচ্ছেদ আনয়ন করিয়াছে; কবি-নামক একটা স্বতন্ত্র জাতির সৃষ্টি করিয়াছে। সম্প্রদায়-বিশেষের হস্তে যখন সাধারণের সম্পত্তি অর্পিত হয়, তখন তাহার স্বার্থ হয় যাহাতে সেটা অন্যের অনায়ত্ত হইয়া উঠে। কবিরাও ভাবের চতুর্দিকে কঠিন বাঁধা নির্মাণ করিয়া কবিত্ব-নামক একটা কৃত্রিম পদার্থ গড়িয়া তুলিয়াছে। কৌশলবিমুগ্ধ জনসাধারণ বিস্ময় রাখিবার স্থান পায় না। এমনি তাহাদের অভ্যাস বিকৃত হইয়া গিয়াছে যে, ছন্দ ও মিল আসিয়া ক্রমাগত হাতুড়ি না পিটাইলে তাহাদের হৃদয়ের চৈতন্য হয় না, স্বাভাবিক সরল ভাষা ত্যাগ করিয়া ভাবকে পাঁচরঙা ছদ্মবেশ ধারণ করিতে হয়। ভাবের পক্ষে এমন হীনতা আর-কিছুই হইতে পারে না। পদ্যটা নাকি আধুনিক সৃষ্টি, সেইজন্য সে হঠাৎ-নবাবের মতো সর্বদাই পেখম তুলিয়া নাচিয়া নাচিয়া বেড়ায়, আমি তাহাকে দু চক্ষে দেখিতে পারি না।

এই বলিয়া ব্যোম পুনর্বার গুড়গুড়ি মুখে দিয়া টানিতে লাগিলেন।

শ্রীমতী দীপ্তি ব্যোমের প্রতি অবজ্ঞাকটাক্ষপাত করিয়া কহিলেন– বিজ্ঞানে প্রাকৃতিক নির্বাচন বলিয়া একটা তত্ত্ব বাহির হইয়াছে। সেই প্রাকৃতিক নির্বাচনের নিয়ম কেবল জন্তুদের মধ্যে নহে, মানুষের রচনার মধ্যেও খাটে। সেই প্রাকৃতিক নির্বাচনের প্রভাবেই ময়ূরীর কলাপের আবশ্যক হয় নাই, ময়ূরের পেখম ক্রমে প্রসারিত হইয়াছে। কবিতার পেখমও সেই প্রাকৃতিক নির্বাচনের ফল, কবিদিগের ষড়যন্ত্র নহে। অসভ্য হইতে সভ্য এমন কোন্‌ দেশ আছে যেখানে কবিত্ব স্বভাবতই ছন্দের মধ্যে বিকশিত হইয়া উঠে নাই।

শ্রীযুক্ত সমীর এতক্ষণ মৃদুহাস্যমুখে চুপ করিয়া বসিয়া শুনিতেছিলেন। দীপ্তি যখন আমাদের আলোচনায় যোগ দিলেন, তখন তাঁহার মাথায় একটা ভাবের উদয় হইল। তিনি একটা সৃষ্টিছাড়া কথার অবতারণা করিলেন। তিনি বলিলেন– কৃত্রিমতাই মনুষ্যের সর্বপ্রধান গৌরব। মানুষ ছাড়া আর-কাহারও কৃত্রিম হইবার অধিকার নাই। গাছকে আপনার পল্লব প্রস্তুত করিতে হয় না, আকাশকে আপনার নীলিমা নির্মাণ করিতে হয় না, ময়ূরের পুচ্ছ প্রকৃতি স্বহস্তে চিত্রিত করিয়া দেন। কেবল মানুষকেই বিধাতা আপনার সৃজনকার্যের অ্যাপ্রেন্টিস করিয়া দিয়াছেন, তাহার প্রতি ছোটোখাটো সৃষ্টির ভার দিয়াছেন। সেই কার্যে যে যত দক্ষতা দেখাইয়াছে সে তত আদর পাইয়াছে। পদ্য গদ্য অপেক্ষা অধিক কৃত্রিম বটে; তাহাতে মানুষের সৃষ্টি বেশি আছে; তাহাতে বেশি রঙ ফলাইতে হইয়াছে, বেশি যত্ন করিতে হইয়াছে। আমাদের মনের মধ্যে যে বিশ্বকর্মা আছেন, যিনি আমাদের অন্তরের নিভৃত সৃজনকক্ষে বসিয়া নানা গঠন নানা বিন্যাস, নানা প্রয়াস, নানা প্রকাশচেষ্টায় সর্বদা নিযুক্ত আছেন, পদ্যে তাঁহারই নিপুণ হস্তের কারুকার্য অধিক আছে। সেই তাঁহার প্রধান গৌরব। অকৃত্রিম ভাষা জলকল্লোলের, অকৃত্রিম ভাষা পল্লবমর্মরের, কিন্তু মন যেখানে আছে সেখানে বহুযত্নরচিত কৃত্রিম ভাষা।

স্রোতস্বিনী অবহিত ছাত্রীর মতো সমীরের সমস্ত কথা শুনিলেন। তাঁহার সুন্দর নম্র মুখের উপর একটা যেন নূতন আলোক আসিয়া পড়িল। অন্য দিন নিজের একটা মত বলিতে যেরূপ ইতস্তত করিতেন, আজ সেরূপ না করিয়া একেবারে আরম্ভ করিলেন– সমীরের কথায় আমার মনে একটা ভাবের উদয় হইয়াছে– আমি ঠিক পরিষ্কার করিয়া বলিতে পারিব কি না জানি না। সৃষ্টির যে অংশের সহিত আমাদের হৃদয়ের যোগ– অর্থাৎ, সৃষ্টির যে অংশ শুদ্ধমাত্র আমাদের মনে জ্ঞান সঞ্চার করে না, হৃদয়ে ভাব সঞ্চার করে যেমন ফুলের সৌন্দর্য, পর্বতের মহত্ত্ব– সেই অংশে কতই নৈপুণ্য খেলাইতে হইয়াছে, কতই রঙ ফলাইতে কত আয়োজন করিতে হইয়াছে! ফুলের প্রত্যেক পাপড়িটিকে কত যত্নে সুগোল সুডোল করিতে হইয়াছে, তাহাকে বৃন্তের উপর কেমন সুন্দর বঙ্কিম ভঙ্গিতে দাঁড় করাইতে হইয়াছে, পর্বতের মাথায় চিরতুষারমুকুট পরাইয়া তাহাকে নীলাকাশের মধ্যে কেমন মহিমার সহিত আসীন করা হইয়াছে, পশ্চিমসমুদ্রতীরের সূর্যাস্তপটের উপর কত রঙের কত তুলি পড়িয়াছে। ভূতল হইতে নভস্তল পর্যন্ত কত সাজসজ্জা, কত রঙচঙ, কত ভাবভঙ্গি, তবে আমাদের এই ক্ষুদ্র মানুষের মন ভুলিয়াছে। ঈশ্বর তাঁহার রচনায় যেখানে প্রেম সৌন্দর্য মহত্ত্ব প্রকাশ করিয়াছেন, সেখানে তাঁহাকেও গুণপনা করিতে হইয়াছে। সেখানে তাঁহাকেও ধ্বনি এবং ছন্দ, বর্ণ এবং গন্ধ বহুযত্নে বিন্যাস করিতে হইয়াছে। অরণ্যের মধ্যে যে ফুল ফুটাইয়াছেন তাহাতে কত পাপড়ির অনুপ্রাস ব্যবহার করিয়াছেন এবং আকাশপটে একটিমাত্র জ্যোতিঃপাত করিতে তাঁহাকে যে কেমন সুনির্দিষ্ট সুসংহত ছন্দ রচনা করিতে হইয়াছে বিজ্ঞান তাহার পদ ও অক্ষর গণনা করিতেছে। ভাব প্রকাশ করিতে মানুষকেও নানা নৈপুণ্য অবলম্বন করিতে হয়। শব্দের মধ্যে সংগীত আনিতে হয়, ছন্দ আনিতে হয়, সৌন্দর্য আনিতে হয়, তবে মনের কথা মনের মধ্যে গিয়া প্রবেশ করে। ইহাকে যদি কৃত্রিমতা বলে, তবে সমস্ত বিশ্বরচনা কৃত্রিম।

এই বলিয়া স্রোতস্বিনী আমার মুখের দিকে চাহিয়া যেন সাহায্য প্রার্থনা করিল– তাহার চোখের ভাবটা এই, আমি কী কতকগুলা বকিয়া গেলাম তাহার ঠিক নাই, তুমি ঐটেকে আর-একটু পরিষ্কার করিয়া বলো-না। এমন সময় ব্যোম হঠাৎ বলিয়া উঠিল– সমস্ত বিশ্বরচনা যে কৃত্রিম এমন মতও আছে। স্রোতস্বিনী যেটাকে ভাবের প্রকাশ বলিয়া বর্ণনা করিতেছেন, অর্থাৎ দৃশ্য শব্দ গন্ধ ইত্যাদি, সেটা যে মায়ামাত্র, অর্থাৎ আমাদের মনের কৃত্রিম রচনা এ কথা অপ্রমাণ করা বড়ো কঠিন।

ক্ষিতি মহা বিরক্ত হইয়া উঠিয়া কহিলেন– তোমরা সকলে মিলিয়া ধান ভানিতে শিবের গান তুলিয়াছ। কথাটা ছিল এই, ভাব প্রকাশের জন্য পদ্যের কোনো আবশ্যক আছে কি না। তোমরা তাহা হইতে একেবারে সমুদ্র পার হইয়া সৃষ্টিতত্ত্ব লয়তত্ত্ব মায়াবাদ প্রভৃতি চোরাবালির মধ্যে গিয়া উত্তীর্ণ হইয়াছে। আমার বিশ্বাস, ভাবপ্রকাশের জন্য ছন্দের সৃষ্টি হয় নাই। ছোটো ছেলেরা যেমন ছড়া ভালোবাসে তাহার ভাবমাধুর্যের জন্য নহে, কেবল তাহার ছন্দোবদ্ধ ধ্বনির জন্য, তেমনি অসভ্য অবস্থায় অর্থহীন কথার ঝংকারমাত্রই কানে ভালো লাগিত। এইজন্য অর্থহীন ছড়াই মানুষের সর্বপ্রথম কবিত্ব। মানুষের এবং জাতির বয়স ক্রমে যতই বাড়িতে থাকে, ততই ছন্দের সঙ্গে অর্থের সংযোগ না করিলে তাহার সম্পূর্ণ তৃপ্তি হয় না। কিন্তু বয়ঃপ্রাপ্ত হইলেও অনেক সময়ে মানুষের মধ্যে দুই-একটা গোপন ছায়াময় স্থানে বালক-অংশ থাকিয়া যায়; ধ্বনিপ্রিয়তা, ছন্দঃপ্রিয়তা সেই গুপ্ত বালকের স্বভাব। আমাদের বয়ঃপ্রাপ্ত অংশ অর্থ চাহে, ভাব চাহে; আমাদের অপরিণত অংশ ধ্বনি চাহে, ছন্দ চাহে।

দীপ্তি গ্রীবা বক্র করিয়া কহিলেন– ভাগ্যে আমাদের সমস্ত অংশ বয়ঃপ্রাপ্ত হইয়া উঠে না। মানুষের নাবালক-অংশটিকে আমি অন্তরের সহিত ধন্যবাদ দিই, তাহারই কল্যাণে জগতে যা কিছু মিষ্টত্ব আছে।

সমীর কহিলেন– যে ব্যক্তি একেবারে পুরোপুরি পাকিয়া গিয়াছে সে’ই জগতের জ্যাঠা ছেলে। কোনো রকমের খেলা, কোনো রকমের ছেলেমানুষি তাহার পছন্দসই নহে। আমাদের আধুনিক হিন্দুজাতটা পৃথিবীর মধ্যে সব চেয়ে জ্যাঠা জাত, অত্যন্ত বেশি মাত্রায় পাকামি করিয়া থাকে, অথচ নানান বিষয়ে কাঁচা। জ্যাঠা ছেলের এবং জ্যাঠা জাতির উন্নতি হওয়া বড়ো দুরূহ; কারণ তাহার মনের মধ্যে নম্রতা নাই। আমার এ কথাটা প্রাইভেট। কোথাও যেন প্রকাশ না হয়। আজকাল লোকের মেজাজ ভালো নয়।

আমি কহিলাম– যখন কলের জাঁতা চালাইয়া শহরের রাস্তা মেরামত হয়, তখন কাষ্ঠফলকে লেখা থাকে– কল চলিতেছে! সাবধান! আমি ক্ষিতিকে পূর্বে হইতে সাবধান করিয়া দিতেছি, আমি কল চালাইব। বাষ্পযানকে তিনি সর্বাপেক্ষা ভয় করেন, কিন্তু সেই কল্পনাবাষ্প যোগে গতিবিধিই আমার সহজসাধ্য বোধ হয়। গদ্যপদ্যের প্রসঙ্গে আমি আর-একবার শিবের গান গাহিব। ইচ্ছা হয় শোনো।

গতির মধ্যে খুব একটা পরিমাণ-করা নিয়ম আছে। পেণ্ডুলম নিয়মিত তালে দুলিয়া থাকে। চলিবার সময় মানুষের পা মাত্রা রক্ষা করিয়া উঠে পড়ে এবং সেই সঙ্গে তাহার সমস্ত অঙ্গপ্রত্যঙ্গ সমান তাল ফেলিয়া গতির সামঞ্জস্য বিধান করিতে থাকে। সমুদ্রতরঙ্গের মধ্যে একটা প্রকাণ্ড লয় আছে। এবং পৃথিবী এক মহাছন্দে সূর্যকে প্রদক্ষিণ করে–

ব্যোমচন্দ্র অকস্মাৎ আমাকে কথার মাঝখানে থামাইয়া বলিতে আরম্ভ করিলেন– স্থিতিই যথার্থ স্বাধীন, সে আপনার অটল গাম্ভীর্যে বিরাজ করে, কিন্তু গতিকে প্রতিপদে আপনাকে নিয়মে বাঁধিয়া চলিতে হয়। অথচ সাধারণের মধ্যে একটা ভ্রান্ত সংস্কার আছে যে, গতিই স্বাধীনতার যথার্থ স্বরূপ এবং স্থিতিই বন্ধন। তাহার কারণ, ইচ্ছাই মনের একমাত্র গতি এবং ইচ্ছা-অনুসারে চলাকেই মূঢ় লোকে স্বাধীনতা বলে। কিন্তু আমাদের পণ্ডিতেরা জানিতেন, ইচ্ছাই আমাদের সকল গতির কারণ, সকল বন্ধনের মূল; এইজন্য মুক্তি অর্থাৎ চরম স্থিতি লাভ করিতে হইলে ওই ইচ্ছাটাকে গোড়া ঘেঁষিয়া কাটিয়া ফেলিতে তাঁহারা বিধান দেন, দেহমনের সর্বপ্রকার গতিরোধ করাই যোগসাধন।

সমীর ব্যোমের পৃষ্ঠে হাত দিয়া সহাস্যে কহিলেন– একটা মানুষ যখন একটা প্রসঙ্গ উত্থাপন করিয়াছে, তখন মাঝখানে তাহার গতিরোধ করার নাম গোলযোগ-সাধন।

আমি কহিলাম– বৈজ্ঞানিক ক্ষিতির নিকট অবিদিত নাই যে, গতির সহিত গতির, এক কম্পনের সহিত অন্য কম্পনের ভারী একটা কুটুম্বিতা আছে। সা সুরের তার বাজিয়া উঠিলে মা সুরের তার কাঁপিয়া উঠে। আলোকতরঙ্গ, উত্তাপতরঙ্গ, ধ্বনিতরঙ্গ, স্নায়ুতরঙ্গ, প্রভৃতি সকলপ্রকার তরঙ্গের মধ্যে এইরূপ একটা আত্মীয়তার বন্ধন আছে। আমাদের চেতনাও একটা তরঙ্গিত কম্পিত অবস্থা। এইজন্য বিশ্বসংসারের বিচিত্র কম্পনের সহিত তাহার যোগ আছে। ধ্বনি আসিয়া তাহার স্নায়ুদোলায় দোল দিয়া যায়, আলোকরশ্মি আসিয়া তাহার স্নায়ুতন্ত্রীতে অলৌকিক অঙ্গুলি আঘাত করে। তাহার চিরকম্পিত স্নায়ুজাল তাহাকে জগতের সমুদয় স্পন্দনের ছন্দে নানাসূত্রে বাঁধিয়া জাগ্রত করিয়া রাখিয়াছে।

হৃদয়ের বৃত্তি, ইংরাজিতে যাহাকে ইমোশন বলে, তাহা আমাদের হৃদয়ের আবেগ, অর্থাৎ গতি; তাহার সহিতও অন্যান্য বিশ্বকম্পনের একটা মহা ঐক্য আছে। আলোকের সহিত, বর্ণের সহিত, ধ্বনির সহিত তাহার একটা স্পন্দনের যোগ, একটা সুরের মিল আছে।

এইজন্য সংগীত এমন অব্যবহিতভাবে আমাদের হৃদয়কে স্পর্শ করিতে পারে, উভয়ের মধ্যে মিলন হইতে অধিক বিলম্ব হয় না। ঝড়ে এবং সমুদ্রে যেমন মাতামাতি হয়, গানে এবং প্রাণে তেমনি একটি নিবিড় সংঘর্ষ হইতে থাকে।

কারণ, সংগীত আপনার কম্পন সঞ্চার করিয়া আমাদের সমস্ত অন্তরকে চঞ্চল করিয়া তোলে। একটা অনির্দেশ্য আবেগে আমাদের প্রাণকে পূর্ণ করিয়া দেয়। মন উদাস হইয়া যায়। অনেক কবি এই অপরূপ ভাবকে অনন্তের জন্য আকাঙক্ষা বলিয়া নাম দিয়া থাকেন। আমিও কখনো কখনো এমনতরো ভাব অনুভব করিয়াছি এবং এমনতরো ভাষাও প্রয়োগ করিয়া থাকিব। কেবল সংগীত কেন, সন্ধ্যাকাশের সূর্যাস্তচ্ছটাও কতবার আমার অন্তরের মধ্যে অনন্ত বিশ্বজগতের হৃৎস্পন্দন সঞ্চারিত করিয়া দিয়াছে; যে-একটি অনির্বচনীয় বৃহৎ সংগীত ধ্বনিত করিয়াছে তাহার সহিত আমার প্রতিদিনের সুখদুঃখের কোনো যোগ নাই, তাহা বিশ্বেশ্বরের মন্দির প্রদক্ষিণ করিতে করিতে নিখিল চরাচরের সামগান। কেবল সংগীত এবং সূর্যাস্ত কেন, যখন কোনো প্রেম আমাদের সমস্ত অস্তিত্বকে বিচলিত করিয়া তোলে, তখন তাহাও আমাদিগকে সংসারের ক্ষুদ্র বন্ধন হইতে বিচ্ছিন্ন করিয়া অনন্তের সহিত যুক্ত করিয়া দেয়। তাহা একটা বৃহৎ উপাসনার আকার ধারণ করে, দেশকালের শিলামুখ বিদীর্ণ করিয়া উৎসের মতো অনন্তের দিকে উৎসারিত হইতে থাকে।

এইরূপে প্রবল স্পন্দনে আমাদিগকে বিশ্বস্পন্দনের সহিত যুক্ত করিয়া দেয়। বৃহৎ সৈন্য যেমন পরস্পরের নিকট হইতে ভাবের উন্মত্ততা আকর্ষণ করিয়া লইয়া একপ্রাণ হইয়া উঠে, তেমনি বিশ্বের কম্পন সৌন্দর্যযোগে যখন আমাদের হৃদয়ের মধ্যে সঞ্চারিত হয় তখন আমরা সমস্ত জগতের সহিত এক তালে পা ফেলিতে থাকি, নিখিলের প্রত্যেক কম্পমান পরমাণুর সহিত এক দলে মিশিয়া অনিবার্য আবেগে অনন্তের দিকে ধাবিত হই।

এই ভাবকে কবিরা কত ভাষায় কত উপায়ে প্রকাশ করিতে চেষ্টা করিয়াছেন এবং কত লোকে তাহা কিছুই বুঝিতে পারে নাই, মনে করিয়াছে উহা কবিদের কাব্যকুয়াশা মাত্র।

কারণ, ভাষার তো হৃদয়ের সহিত প্রত্যক্ষ যোগ নাই, তাহাকে মস্তিষ্ক ভেদ করিয়া অন্তরে প্রবেশ করিতে হয়। সে দূতমাত্র, হৃদয়ের খাস-মহলে তাহার অধিকার নাই, আম-দরবারে আসিয়া সে আপনার বার্তা জানাইয়া যায় মাত্র। তাহাকে বুঝিতে, অর্থ করিতে অনেকটা সময় যায়। কিন্তু সংগীত একেবারে এক ইঙ্গিতেই হৃদয়কে আলিঙ্গন করিয়া ধরে।

